public class UserFile {

	private String name;
	private Long size;

	public void setName(String name) {
		this.name = name;
	}

	public void setSize(Long size) {
		this.size = size;
	}

	public String getName() {
		return name;
	}

	public Long getSize() {
		return size;
	}
}
